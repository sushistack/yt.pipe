func TestPluginSwap_ImageGenProvider(t *testing.T) {
	configDir := t.TempDir()

	// 1. Generate config with siliconflow as imagegen provider
	result := &wizardResult{
		SCPDataPath:      "/data/scp",
		WorkspacePath:    "/data/workspace",
		LLMProvider:      "openai",
		ImageGenProvider: "siliconflow",
		TTSProvider:      "openai",
		OutputProvider:   "capcut",
	}
	err := generateConfig(result, configDir)
	require.NoError(t, err)

	configPath := filepath.Join(configDir, "config.yaml")

	// 2. Read the generated file
	content, err := os.ReadFile(configPath)
	require.NoError(t, err)

	// 3. Replace "siliconflow" with "dall-e"
	original := string(content)
	modified := strings.ReplaceAll(original, `"siliconflow"`, `"dall-e"`)
	require.NotEqual(t, original, modified, "replacement should have changed the content")

	// 4. Write modified file back
	err = os.WriteFile(configPath, []byte(modified), 0600)
	require.NoError(t, err)

	// 5. Load with config.Load using explicit path
	loaded, err := config.Load(configPath)
	require.NoError(t, err)

	// 6. Assert the new provider is recognized
	assert.Equal(t, "dall-e", loaded.Config.ImageGen.Provider)
}

