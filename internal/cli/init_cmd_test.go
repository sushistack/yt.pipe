func TestPluginSwap_ConfigReload(t *testing.T) {
	configDir := t.TempDir()

	// Generate config with initial providers
	result := &wizardResult{
		SCPDataPath:      "/data/scp",
		WorkspacePath:    "/data/workspace",
		LLMProvider:      "openai",
		ImageGenProvider: "siliconflow",
		TTSProvider:      "openai",
		OutputProvider:   "capcut",
	}
	err := generateConfig(result, configDir)
	require.NoError(t, err)

	configPath := filepath.Join(configDir, "config.yaml")

	// Read generated file
	content, err := os.ReadFile(configPath)
	require.NoError(t, err)
	original := string(content)

	// Modify imagegen provider: siliconflow -> stable-diffusion
	modified := strings.ReplaceAll(original, `"siliconflow"`, `"stable-diffusion"`)
	// Modify TTS provider: openai -> google (within tts block only)
	modified = strings.Replace(modified, "tts:\n  provider: \"openai\"", "tts:\n  provider: \"google\"", 1)
	require.NotEqual(t, original, modified, "both replacements should have changed the content")

	// Write modified file back
	err = os.WriteFile(configPath, []byte(modified), 0600)
	require.NoError(t, err)

	// Reload config with explicit path
	loaded, err := config.Load(configPath)
	require.NoError(t, err)

	// Assert both new providers are recognized
	assert.Equal(t, "stable-diffusion", loaded.Config.ImageGen.Provider)
	assert.Equal(t, "google", loaded.Config.TTS.Provider)
	// LLM provider should be unchanged
	assert.Equal(t, "openai", loaded.Config.LLM.Provider)
}
