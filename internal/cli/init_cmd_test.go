func TestWizard_FullFlow(t *testing.T) {
	// Create temp dirs for path validation
	tmpDir := t.TempDir()
	scpDir := filepath.Join(tmpDir, "scp-data")
	workDir := filepath.Join(tmpDir, "workspace")

	// Simulate user input:
	// 1. Overwrite prompt won't appear (no existing config in this test path)
	// 2. SCP data path (doesn't exist, will be asked to create)
	// 3. Confirm create SCP dir -> y
	// 4. Workspace path (doesn't exist, will be asked to create)
	// 5. Confirm create workspace dir -> y
	// 6. LLM API key
	// 7. Skip validation -> y
	// 8. ImageGen API key
	// 9. Skip validation -> y
	// 10. TTS provider selection -> 1 (openai)
	// 11. TTS API key
	// 12. Skip validation -> y
	input := strings.Join([]string{
		scpDir,          // SCP data path
		"y",             // create dir
		workDir,         // workspace path
		"y",             // create dir
		"sk-test-llm",   // LLM API key
		"y",             // skip validation
		"sf-test-img",   // ImageGen API key
		"y",             // skip validation
		"1",             // TTS provider: openai
		"sk-test-tts",   // TTS API key
		"y",             // skip validation
	}, "\n")
	input += "\n"

	r := strings.NewReader(input)
	w := &bytes.Buffer{}

	// Override config dir to temp
	origDefaultConfigDir := defaultConfigDirFn
	defaultConfigDirFn = func() (string, error) {
		return filepath.Join(tmpDir, ".yt-pipe"), nil
	}
	defer func() { defaultConfigDirFn = origDefaultConfigDir }()

	err := runWizard(r, w, true)
	require.NoError(t, err)

	output := w.String()

	// Verify wizard output contains expected sections
	assert.Contains(t, output, "Setup Wizard")
	assert.Contains(t, output, "LLM Plugin")
	assert.Contains(t, output, "Image Generation Plugin")
	assert.Contains(t, output, "TTS Plugin")
	assert.Contains(t, output, "Output Assembler")
	assert.Contains(t, output, "Setup Summary")
	assert.Contains(t, output, "Next Steps")

	// Verify directories were created
	_, err = os.Stat(scpDir)
	assert.NoError(t, err)
	_, err = os.Stat(workDir)
	assert.NoError(t, err)

	// Verify config file was generated
	configPath := filepath.Join(tmpDir, ".yt-pipe", "config.yaml")
	content, readErr := os.ReadFile(configPath)
	require.NoError(t, readErr)

	text := string(content)
	assert.Contains(t, text, scpDir)
	assert.Contains(t, text, workDir)

	// API keys must NOT be in config file
	assert.NotContains(t, text, "sk-test-llm")
	assert.NotContains(t, text, "sf-test-img")
	assert.NotContains(t, text, "sk-test-tts")
}

