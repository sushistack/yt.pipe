// newTestInitCmd builds a *cobra.Command with the same flags registered in
// the real initCmd.init() function, so tests can exercise runWizardNonInteractive
// without touching the global cobra command tree.
func newTestInitCmd() *cobra.Command {
	cmd := &cobra.Command{Use: "init"}
	cmd.Flags().Bool("force", false, "")
	cmd.Flags().Bool("non-interactive", false, "")
	cmd.Flags().String("scp-data-path", "", "")
	cmd.Flags().String("workspace-path", "", "")
	cmd.Flags().String("llm-api-key", "", "")
	cmd.Flags().String("imagegen-api-key", "", "")
	cmd.Flags().String("tts-provider", "", "")
	cmd.Flags().String("tts-api-key", "", "")
	return cmd
}

